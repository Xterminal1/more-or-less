def more_or_less_game():
    score = 0
    nation1 = random.choice(list(nations.keys()))

    while 1:
        # Select a second nation2
        nation2 = random.choice(list(nations.keys()))
        # Ensure the two nations are not the s1ame
        while nation2 == nation1:
            nation2 = random.choice(list(nations.keys()))

        # Show the first nation and its population
        print(f"{nation1}: {nations[nation1]:,}")
        guess = input(f"{nation2}: ")

        # Compare populations
        if (guess == "m" and nations[nation2] > nations[nation1]) or (guess == "l" and nations[nation2] < nations[nation1]):
            score += 1
            print(f"                                         Score: {score}")

            nation1 = nation2  # Make the second nation the new first nation
        else:
            print(f"Wrong! {nation2} is {nations[nation2]:,}.")
            print(f"Final score: {score}")
            break
            
if __name__ == "__main__":
    more_or_less_game()
